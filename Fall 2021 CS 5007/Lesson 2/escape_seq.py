def main():

    # Backspace
    print("Backspace example")
    print("Text_\b_Text")
    print()

    # Tab
    print("Tab example:")
    print("Text_\t_Text")
    print()

    # Linefeed
    print("Linefeed example:")
    print("Text_\n_Text")
    print()

    # Carriage Return
    print("Carriage return example:")
    print("Text_\r_Text")
    print()

    # Backslash
    print("Backslash example:")
    print("Text_\\_Text")
    print()

    # Single Quote
    print("Single quote example")
    print("Text_\'_Text")
    print()

    # Double Quote
    print("Double quote example")
    print("Text_\"_Text")
    print()

    # Bell
    print("Bell")
    print("\a")

if __name__ == "__main__":
    main()
